package com.food.order.model;

public class Labour {
	private int  labourId;
	private String labourName;
	private String labourDesignation;
	private String labourEmail;
	private int labourPhoneNumber;
	
	public int getLabourId() {
		return labourId;
	}
	public void setLabourId(int labourId) {
		this.labourId = labourId;
	}
	public String getLabourName() {
		return labourName;
	}
	public void setLabourName(String labourName) {
		this.labourName = labourName;
	}
	public String getLabourDesignation() {
		return labourDesignation;
	}
	public void setLabourDesignation(String labourDesignation) {
		this.labourDesignation = labourDesignation;
	}
	public String getLabourEmail() {
		return labourEmail;
	}
	public void setLabourEmail(String labourEmail) {
		this.labourEmail = labourEmail;
	}
	public int getLabourPhoneNumber() {
		return labourPhoneNumber;
	}
	public void setLabourPhoneNumber(int labourPhoneNumber) {
		this.labourPhoneNumber = labourPhoneNumber;
	}
}
